public class Lab1 {
   public static void Line() {
      System.out.println ("+---------+");
   }
      
   public static void Top(){
      System.out.println ("|    *    |");
      System.out.println ("|   /*\\   |");
      System.out.println ("|  //*\\\\  |");
      System.out.println ("| ///*\\\\\\ |");
   }
      
   public static void Bottom() {
      System.out.println ("| \\\\\\*/// |");
      System.out.println ("|  \\\\*//  |");
      System.out.println ("|   \\*/   |");
      System.out.println ("|    *    |");
   }
      
   public static void main(String[] agrs) {
      Line();
      Top();
      Bottom();
      Line();
      Bottom();
      Top();
      Line();
   }
}
    
    
